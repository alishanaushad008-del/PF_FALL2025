#include<stdio.h>
int main(){
    float height,width,total_inches,total_feet;
     printf("Enter the height of the bookcase (in inches): ");
    scanf("%f", &height);

    printf("Enter the width of the bookcase (in inches): ");
    scanf("%f", &width);
 // Total board length in inches:
 // 2 sides (height) + top + bottom + 3 shelves (width)
total_inches = (2 * height) + (5 * width);
  total_feet = total_inches / 12;
printf("Joe will need %.2f feet of 12-inch-wide boards to build the bookcase.\n", total_feet);


}